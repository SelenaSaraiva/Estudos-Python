# Biblioteca random (gerar números aleatorios entre 0 e 1)

# Gerando números aleatórios do próprio computador
import random

num = random.random()
print(num)

# Gerando um número, inteiro, do próprio computador
import random

num = random.randint(1, 10)  # escolho de onde a onde ele deve gerar
print(num)
