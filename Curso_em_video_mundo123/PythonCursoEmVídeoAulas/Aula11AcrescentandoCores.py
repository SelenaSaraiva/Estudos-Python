#Negrito e letras vermelhas;

print('\033[1;31;mOlá mundo!')

#Sublinhado; letras roxas com o fundo amarelo;

print('\033[4;35;43mOlá mundo!')

#Sem modificanção na fonte; letra ciano e fundo azul; cores apenas nas letras;

print('\033[0;36;44mOlá mundo!\033[m')

# Inverter: fundo branco e letra preta;

print('\033[7;30mOlá mundo!')