nome = input('Qual é seu nome? ')
print('Prazer em te conhecer, {}!'.format(nome))

print('='*30)

print('Prazer em te conhecer, {:20}!'.format(nome))

print('='*30)

# Dar 20 espaços após a informação.

print('Prazer em te conhecer, {:>20}!'.format(nome))

print('='*30)

# Dar 20 espaços após a informação com alinhamento para direita.

print('Prazer em te conhecer, {:<20}!'.format(nome))

print('='*30)

# Dar 20 espaços após a informação com alinhamento para a esquerda.

print('Prazer em te conhecer, {:^20}!'.format(nome))

print('='*30)

# Dar 20 espaços e deixar a informação centralizada.

print('Prazer em te conhecer, {:=^20}!'.format(nome))

print('='*30)

# Dar 20 espaços, deixar a informação centralizada e separada por sinal de igual.







