# Leia um número inteiro e mostre o seu sobro, tiplo e sua raiz quadrada

num = int(input("Informe um número: "))

dobro = num * 2
triplo = num * 3
raiz = num ** (1/2)

print('O dobro de {} é igual a: {} \n O triplo de {} é igual a : {} \n A raiz quadrada de {} é igual a: {}'.format(num, dobro, num, triplo, num, raiz))


